package com.android.wilcoconnect.model.leave;

public class MyLeaveData {

    public String LeaveType;

    public String AppliedDate;

    public String FromDate;

    public String ToDate;

    public int No_of_Counts;

    public String Remarks;

    public String LeaveStatus;

    public String ApprovedDate;

    public String RemarksByApprover;

    public String Approver;

    public String getLeaveType() {
        return LeaveType;
    }

    public void setLeaveType(String leaveType) {
        LeaveType = leaveType;
    }

    public String getAppliedDate() {
        return AppliedDate;
    }

    public void setAppliedDate(String appliedDate) {
        AppliedDate = appliedDate;
    }

    public String getFromDate() {
        return FromDate;
    }

    public void setFromDate(String fromDate) {
        FromDate = fromDate;
    }

    public String getToDate() {
        return ToDate;
    }

    public void setToDate(String toDate) {
        ToDate = toDate;
    }

    public int getNo_of_Counts() {
        return No_of_Counts;
    }

    public void setNo_of_Counts(int no_of_Counts) {
        No_of_Counts = no_of_Counts;
    }

    public String getRemarks() {
        return Remarks;
    }

    public void setRemarks(String remarks) {
        Remarks = remarks;
    }

    public String getLeaveStatus() {
        return LeaveStatus;
    }

    public void setLeaveStatus(String leaveStatus) {
        LeaveStatus = leaveStatus;
    }

    public String getApprovedDate() {
        return ApprovedDate;
    }

    public void setApprovedDate(String approvedDate) {
        ApprovedDate = approvedDate;
    }

    public String getRemarksByApprover() {
        return RemarksByApprover;
    }

    public void setRemarksByApprover(String remarksByApprover) {
        RemarksByApprover = remarksByApprover;
    }

    public String getApprover() {
        return Approver;
    }

    public void setApprover(String approver) {
        Approver = approver;
    }
}
