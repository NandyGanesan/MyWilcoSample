package com.android.wilcoconnect.model.leave;

public class LeaveAvailableTable {

    public String LeaveType;

    public String LeaveTaken;

    public String LeaveAvailability;

    public String LeaveApplied;

    public String getLeaveType() {
        return LeaveType;
    }

    public void setLeaveType(String leaveType) {
        LeaveType = leaveType;
    }

    public String getLeaveTaken() {
        return LeaveTaken;
    }

    public void setLeaveTaken(String leaveTaken) {
        LeaveTaken = leaveTaken;
    }

    public String getLeaveAvailability() {
        return LeaveAvailability;
    }

    public void setLeaveAvailability(String leaveAvailability) {
        LeaveAvailability = leaveAvailability;
    }

    public String getLeaveApplied() {
        return LeaveApplied;
    }

    public void setLeaveApplied(String leaveApplied) {
        LeaveApplied = leaveApplied;
    }
}
